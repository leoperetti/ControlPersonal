function inicializar() {
    var d = new Date();
    var strDate = d.getDate() + "/" + (d.getMonth() + 1) + "/" + d.getFullYear();
    $("#Fecha").val(strDate);
    $("#Fecha").datepicker();
    
}

function partialComercio() {

    $("#tipoComercio").click(function () {

        $("#divComercio").show();

    });

    $("#cerrarComercio").click(function () {

        $("#divComercio").hide();

    });
}
